
%load HPLCforweb
%fdata = HPLCforweb.data;

load oliveoil

numdim = 6;
numdata = 120;

data = Y(1:numdata,1:numdim);
fname = ['oliveoil/s' num2str(numdim) 'x' num2str(numdata)];

opttu = nrmi.QPYPOptionPack() ...
        .parse({'-PYPSampler','TU', ...
                'ThetaShape','1000000','ThetaInvScale','1000000', ...
                'sigmaAlpha','1000000','sigmaBeta',    '2000000'}) ...
        .getQRM();
optvz = nrmi.QPYPOptionPack() ...
        .parse({'-PYPSampler','VZ', ...
                'ThetaShape','1000000','ThetaInvScale','1000000', ...
                'sigmaAlpha','1000000','sigmaBeta',    '2000000'}) ...
        .getQRM();
optmarg=nrmi.QPYPOptionPack() ...
        .parse({'-PYPSampler','marg', ...
                'ThetaShape','1000000','ThetaInvScale','1000000', ...
                'sigmaAlpha','1000000','sigmaBeta',    '2000000'}) ...
        .getQRM();

pitmanyortu = runqmix('Reuse',opttu,data,false,'olive',10000,10000,10,10);
pitmanyorvz = runqmix('Reuse',optvz,data,false,'olive',10000,10000,10,10);
pitmanyormarg = runqmix('Reuse',optmarg,data,false,'olive',10000,10000,10,10);
